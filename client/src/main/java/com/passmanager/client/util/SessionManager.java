package com.passmanager.client.util;

/*
Класс-синглтон (в виде статического хранилища) для управления данными сессии.
Служит глобальной точкой доступа к мастер-паролю, чтобы не передавать его 
между контроллерами вручную.
*/
public class SessionManager {

    /* Хранит мастер-пароль в оперативной памяти (RAM). 
    Поле статическое, поэтому данные сохраняются на протяжении всего 
    цикла работы приложения.
    */
    private static String masterPassword;

    /*
    Запоминает пароль после успешного ввода пользователем на экране логина.
    Этот пароль в дальнейшем используется как ключ для AES-алгоритма.
    */
    public static void setMasterPassword(String password) {
        masterPassword = password;
    }

    /*
    Позволяет любому компоненту (например, EncryptionUtil или MainController) 
    получить доступ к ключу для расшифровки или шифрования записей.
    */
    public static String getMasterPassword() {
        return masterPassword;
    }

    /*
    Принудительное обнуление данных сессии. 
    Используется при логауте или закрытии приложения, чтобы чувствительные 
    данные не оставались в памяти дольше необходимого.
    */
    public static void clean() {
        masterPassword = null;
    }
}